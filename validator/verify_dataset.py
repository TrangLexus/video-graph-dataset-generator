#!/usr/bin/env python3
"""
verify_dataset.py -- Utility to validate VideoGraphDB CSV data.

This script reads the dataset generated by the data generator and performs
basic sanity checks:

1. Verify that CSV files contain expected headers.
2. Validate structural edges (NEAR_BY, LOCATED_AT, RECORDED_BY, NEXT_TO) have
   appropriate blank or filled fields according to the specification.
3. Validate temporal edges (DETECTED_IN, CARRIES, USES, INTERACTS_WITH) have
   proper timestamps and identifiers.
4. Count rows in partitioned data and compare with the root files.

Usage:
    python verify_dataset.py --data_dir /path/to/output
"""
import argparse
import csv
import os
from collections import defaultdict
from datetime import datetime

# Additional imports for advanced validation
import json

import pandas as pd


def read_csv(path):
    with open(path, newline="", encoding="utf-8") as f:
        reader = csv.reader(f)
        header = next(reader)
        rows = [row for row in reader]
    return header, rows


def verify_headers(headers, expected):
    ok = True
    missing = [h for h in expected if h not in headers]
    extra = [h for h in headers if h not in expected]
    if missing:
        print(f"[ERROR] Missing columns: {missing}")
        ok = False
    if extra:
        print(f"[WARN] Extra columns: {extra}")
    return ok


def check_structural_edges(row):
    # row: [source_id, dest_id, type, ts_start, ts_end, date, tw_id,
    #       partition_id, camera_id, location_id, confidence, bbox, description]
    edge_type = row[2]
    ts_start, ts_end, date, tw_id = row[3], row[4], row[5], row[6]
    camera_id, loc_id, conf, bbox = row[8], row[9], row[10], row[11]
    desc = row[12]
    if edge_type == "NEAR_BY":
        if any([ts_start, ts_end, date, tw_id, camera_id, loc_id, conf, bbox]):
            return False, "NEAR_BY should not have timestamps or context"
        if not row[7]:
            return False, "NEAR_BY missing partition_id"
        return True, ""
    if edge_type == "LOCATED_AT":
        # camera_id and location_id should match source/dest
        if any([ts_start, ts_end, date, tw_id, conf, bbox]):
            return False, "LOCATED_AT should not have timestamps"
        if camera_id != row[0] or loc_id != row[1]:
            return False, "LOCATED_AT should repeat camera and location IDs"
        return True, ""
    if edge_type == "RECORDED_BY":
        if not ts_start or not ts_end or not date:
            return False, "RECORDED_BY requires ts_start, ts_end, and date"
        if tw_id:
            return False, "RECORDED_BY tw_id should be blank"
        if not camera_id or not loc_id:
            return False, "RECORDED_BY missing camera_id/location_id"
        return True, ""
    if edge_type == "NEXT_TO":
        # ts_start and ts_end must be equal; date must not be empty
        if not date:
            return False, "NEXT_TO requires date"
        if ts_start != ts_end:
            return False, "NEXT_TO ts_start and ts_end should be equal"
        return True, ""
    return None, ""


def check_temporal_edge(row):
    edge_type = row[2]
    ts_start, ts_end, date, tw_id = row[3], row[4], row[5], row[6]
    camera_id, loc_id = row[8], row[9]
    # Parse timestamps if present
    try:
        dt_start = datetime.fromisoformat(ts_start) if ts_start else None
        dt_end = datetime.fromisoformat(ts_end) if ts_end else None
    except Exception:
        return False, f"Invalid datetime format in temporal edge {edge_type}"
    # Temporal ordering rules
    if dt_start and dt_end:
        if edge_type == "NEXT_TO":
            # For NEXT_TO edges, ts_start and ts_end must be equal
            if dt_start != dt_end:
                return False, "NEXT_TO ts_start and ts_end should be equal"
        else:
            # For all other temporal edges, start must be strictly before end
            if dt_start >= dt_end:
                return False, f"ts_start >= ts_end in edge {edge_type}"
    if edge_type == "DETECTED_IN":
        # DETECTED_IN edges must provide camera_id and location_id
        if not camera_id or not loc_id:
            return False, "DETECTED_IN missing camera_id/location_id"
    # Validate tw_id matches ts_start bucket if ts_start provided
    if dt_start is not None and tw_id:
        try:
            # Compute bucket index based on seconds since day's midnight
            date_part = date
            # date might be empty for some edges; skip check if date missing
            if date_part:
                base_dt = datetime.fromisoformat(date_part)
                # Set base to midnight of the day
                base_dt = base_dt.replace(hour=0, minute=0, second=0, microsecond=0)
                delta_seconds = int((dt_start - base_dt).total_seconds())
                bucket = delta_seconds // 10
                expected_tw = f"TW{bucket:04d}"
                if expected_tw != tw_id:
                    return False, f"tw_id {tw_id} does not match ts_start bucket {expected_tw}"
        except Exception:
            # Ignore bucket check if any error occurs
            pass
    return True, ""


def verify_partitions(root_dir):
    ok = True
    # Check rels.csv row counts
    root_rels = os.path.join(root_dir, "rels.csv")
    _, root_rows = read_csv(root_rels)
    total_rows = len(root_rows)
    part_dir = os.path.join(root_dir, "by_partition")
    sum_rows = 0
    for p in os.listdir(part_dir):
        rel_path = os.path.join(part_dir, p, "rels.csv")
        if os.path.exists(rel_path):
            _, rows = read_csv(rel_path)
            sum_rows += len(rows)
    if sum_rows != total_rows:
        print(f"[ERROR] Sum of rows in partition rels.csv ({sum_rows}) does not match root rels.csv ({total_rows})")
        ok = False
    return ok


def verify_dataset(data_dir):
    """
    Main entry point for validating a generated dataset directory.

    This function performs a suite of checks, collecting results and
    summarising them as a JSON-like dict printed at the end.  In
    addition to the legacy structural and temporal checks, it
    implements the data quality pipeline described in the project
    specification.  The checks include:

    * Header compliance for each CSV
    * Structural and temporal schema validation (ts_start < ts_end, proper
      fields blank/filled, tw_id bucket calculation)
    * Temporal overlap between semantic edges and DETECTED_IN intervals
    * Completeness of edge types
    * Partition integrity across all relevant files
    * Data distribution sanity (pose states, vehicle speeds, confidence and
      bbox validity)
    * Interaction ratio calculation for INTERACTS_WITH
    * Movement detection across partitions

    At the end, a summary dictionary with PASS/FAIL flags and metrics
    will be printed.
    """
    schema_ok = True  # tracks structural/time validity
    print(f"Verifying dataset at: {data_dir}\n")

    # Expected headers for various CSVs
    expected_rels = [
        "source_id",
        "destination_id",
        "type",
        "ts_start",
        "ts_end",
        "date",
        "tw_id",
        "partition_id",
        "camera_id",
        "location_id",
        "confidence",
        "bbox",
        "description",
    ]
    expected_person_tw = [
        "pid_tw",
        "id_global",
        "date",
        "tw_id",
        "partition_id",
        "shirt_color",
        "pant_color",
        "pose_state",
    ]
    expected_thing_tw = [
        "tid_tw",
        "id_global",
        "date",
        "tw_id",
        "partition_id",
        "thing_type",
        "size_category",
        "base_color",
        "state",
    ]
    expected_vehicle_tw = [
        "vid_tw",
        "id_global",
        "date",
        "tw_id",
        "partition_id",
        "vehicle_type",
        "base_color",
        "speed_kmh",
        "direction",
    ]

    # ---------------------------
    # Header and schema checks
    # ---------------------------
    rels_path = os.path.join(data_dir, "rels.csv")
    header, rows = read_csv(rels_path)
    if not verify_headers(header, expected_rels):
        schema_ok = False
    # Validate each row in rels.csv for structural and temporal correctness
    for idx, row in enumerate(rows, start=2):
        etype = row[2]
        # Skip blank type rows (should not occur in well-formed data)
        if not etype:
            continue
        structural_check = check_structural_edges(row)
        if structural_check[0] is False:
            schema_ok = False
            print(
                f"[ERROR] Structural edge error at rels.csv line {idx}: {structural_check[1]}"
            )
            continue
        # Temporal edges (DETECTED_IN, CARRIES, USES, INTERACTS_WITH, RECORDED_BY, NEXT_TO)
        temporal_ok, msg = check_temporal_edge(row)
        if not temporal_ok:
            schema_ok = False
            print(
                f"[ERROR] Temporal edge error at rels.csv line {idx}: {msg}"
            )

    # Verify node TW headers
    for fname, expected in [
        ("nodes_person_TW.csv", expected_person_tw),
        ("nodes_thing_TW.csv", expected_thing_tw),
        ("nodes_vehicle_TW.csv", expected_vehicle_tw),
    ]:
        path = os.path.join(data_dir, fname)
        hdr, _ = read_csv(path)
        if not verify_headers(hdr, expected):
            schema_ok = False

    # Verify tw_id corresponds to start_time in nodes_timewindow.csv
    tw_path = os.path.join(data_dir, "nodes_timewindow.csv")
    if os.path.exists(tw_path):
        hdr, rows_tw = read_csv(tw_path)
        # Expect columns: date, tw_id, start_time, end_time, duration_seconds
        try:
            idx_date = hdr.index("date")
            idx_tw = hdr.index("tw_id")
            idx_start = hdr.index("start_time")
        except ValueError:
            print("[ERROR] nodes_timewindow.csv missing expected columns")
            schema_ok = False
        else:
            for idx, row in enumerate(rows_tw, start=2):
                date_part = row[idx_date]
                tw_id_val = row[idx_tw]
                st = row[idx_start]
                try:
                    # Compose full timestamp and derive bucket
                    start_dt = datetime.fromisoformat(date_part + " " + st)
                    base_dt = datetime.fromisoformat(date_part)
                    bucket = int((start_dt - base_dt).total_seconds()) // 10
                    expected_tw = f"TW{bucket:04d}"
                    if expected_tw != tw_id_val:
                        schema_ok = False
                        print(
                            f"[ERROR] nodes_timewindow.csv line {idx}: tw_id {tw_id_val} does not match expected {expected_tw}"
                        )
                except Exception:
                    schema_ok = False
                    print(
                        f"[ERROR] Invalid datetime in nodes_timewindow.csv at line {idx}"
                    )

    # ---------------------------
    # Load CSVs into pandas DataFrames for advanced checks
    # Using dtype=str and keep_default_na=False to preserve empty strings
    # for later custom parsing
    df_rels = pd.read_csv(rels_path, dtype=str, keep_default_na=False)
    df_person_tw = pd.read_csv(
        os.path.join(data_dir, "nodes_person_TW.csv"), dtype=str, keep_default_na=False
    )
    df_thing_tw = pd.read_csv(
        os.path.join(data_dir, "nodes_thing_TW.csv"), dtype=str, keep_default_na=False
    )
    df_vehicle_tw = pd.read_csv(
        os.path.join(data_dir, "nodes_vehicle_TW.csv"), dtype=str, keep_default_na=False
    )

    # ---------------------------
    # Advanced check functions
    # ---------------------------
    def check_temporal_overlap(df_edges: pd.DataFrame, df_detected: pd.DataFrame):
        """Return list of semantic edges violating temporal overlap constraint."""
        errors = []
        # Build mapping of detection intervals per entity
        det_map = defaultdict(list)
        for _, row in df_detected.iterrows():
            sid = row["source_id"]
            ts_start = row["ts_start"]
            ts_end = row["ts_end"]
            if ts_start and ts_end:
                try:
                    s_dt = pd.to_datetime(ts_start, errors="raise")
                    e_dt = pd.to_datetime(ts_end, errors="raise")
                    det_map[sid].append((s_dt, e_dt))
                except Exception:
                    # skip invalid detection intervals
                    continue
        # Evaluate each semantic edge
        for _, row in df_edges.iterrows():
            etype = row["type"]
            if etype not in {"CARRIES", "USES", "INTERACTS_WITH"}:
                continue
            sid = row["source_id"]
            did = row["destination_id"]
            ts_start = row["ts_start"]
            ts_end = row["ts_end"]
            # Require non-empty timestamps
            if not ts_start or not ts_end:
                errors.append((sid, did, etype, ts_start, ts_end))
                continue
            try:
                s_edge = pd.to_datetime(ts_start, errors="raise")
                e_edge = pd.to_datetime(ts_end, errors="raise")
            except Exception:
                errors.append((sid, did, etype, ts_start, ts_end))
                continue
            found = False
            # Retrieve detection intervals for both participants
            for s_interval in det_map.get(sid, []):
                for d_interval in det_map.get(did, []):
                    overlap_start = max(s_interval[0], d_interval[0])
                    overlap_end = min(s_interval[1], d_interval[1])
                    if s_edge >= overlap_start and e_edge <= overlap_end:
                        found = True
                        break
                if found:
                    break
            if not found:
                errors.append((sid, did, etype, ts_start, ts_end))
        return errors

    def check_edge_types(df_edges: pd.DataFrame):
        """Return set of expected edge types that are missing in rels.csv."""
        expected = {
            "DETECTED_IN",
            "RECORDED_BY",
            "LOCATED_AT",
            "NEAR_BY",
            "NEXT_TO",
            "CARRIES",
            "USES",
            "INTERACTS_WITH",
        }
        actual = set(df_edges["type"].unique())
        return expected - actual

    def check_partition_integrity_all(root_dir: str, files: list):
        """
        Ensure that for each listed CSV file the total number of rows in
        the partition folders equals the number of rows in the root CSV.
        Returns True if all files are consistent, False otherwise.
        """
        ok_flag = True
        part_root = os.path.join(root_dir, "by_partition")
        for fname in files:
            global_path = os.path.join(root_dir, fname)
            if not os.path.exists(global_path):
                continue
            # count rows excluding header in global file
            with open(global_path, newline="", encoding="utf-8") as f:
                total_rows = sum(1 for _ in f) - 1
            part_sum = 0
            if os.path.isdir(part_root):
                for p in os.listdir(part_root):
                    part_path = os.path.join(part_root, p, fname)
                    if os.path.exists(part_path):
                        with open(part_path, newline="", encoding="utf-8") as pf:
                            part_rows = sum(1 for _ in pf) - 1
                            part_sum += part_rows
            if part_sum != total_rows:
                print(
                    f"[ERROR] Sum of rows in partitions for {fname} ({part_sum}) does not match root file ({total_rows})"
                )
                ok_flag = False
        return ok_flag

    def check_data_distribution(df_person: pd.DataFrame, df_vehicle: pd.DataFrame, df_edges: pd.DataFrame):
        """
        Validate distributions and ranges of certain fields.  Returns
        True if all checks pass, otherwise False.
        """
        flag = True
        # Pose state distribution: ensure only expected pose states appear
        valid_pose = {"Standing", "Walking", "Running", "Sitting"}
        actual_poses = set(df_person["pose_state"].dropna().unique())
        invalid_poses = actual_poses - valid_pose
        if invalid_poses:
            print(f"[ERROR] Invalid pose_state values detected: {invalid_poses}")
            flag = False
        # Vehicle speeds: convert to numeric and check range
        def to_float(x):
            try:
                return float(x)
            except Exception:
                return None
        speed_vals = df_vehicle["speed_kmh"].apply(to_float)
        if not speed_vals.dropna().between(0, 60).all():
            print(
                "[ERROR] Vehicle speeds out of expected 0-60 km/h range detected"
            )
            flag = False
        # Edge confidence: numeric between 0.7 and 0.99 for edges with confidence specified
        if "confidence" in df_edges.columns:
            conf_vals = []
            for c in df_edges["confidence"]:
                if c:
                    try:
                        conf_vals.append(float(c))
                    except Exception:
                        conf_vals.append(None)
            # Filter None values; only enforce range for numeric confidences
            if conf_vals:
                valid_conf = [v for v in conf_vals if v is not None]
                if valid_conf:
                    if not pd.Series(valid_conf).between(0.7, 0.99).all():
                        print(
                            "[ERROR] Edge confidence values outside expected range [0.7, 0.99] detected"
                        )
                        flag = False
        # Bbox JSON validity: for non-empty bbox strings ensure valid JSON
        if "bbox" in df_edges.columns:
            invalid_bbox_count = 0
            for b in df_edges["bbox"]:
                if b:
                    try:
                        json.loads(b)
                    except Exception:
                        invalid_bbox_count += 1
            if invalid_bbox_count > 0:
                print(f"[ERROR] {invalid_bbox_count} bbox fields contain invalid JSON")
                flag = False
        return flag

    def check_interaction_ratio(df_edges: pd.DataFrame, n_persons: int):
        """
        Compute the global interaction ratio across all Person_TW nodes.

        This legacy method calculates the number of INTERACTS_WITH edges
        divided by the total number of possible pairs among all Person_TW
        instances in the dataset.  For large datasets the denominator becomes
        extremely large, so the resulting ratio may be very small and round
        down to zero.  It is retained for backward compatibility but is
        superseded by calculate_interaction_ratio_per_tw.
        """
        possible = n_persons * (n_persons - 1) / 2 if n_persons > 1 else 0
        actual = len(df_edges[df_edges["type"] == "INTERACTS_WITH"])
        return (actual / possible) if possible > 0 else 0.0

    def calculate_interaction_ratio_per_tw(df_edges: pd.DataFrame, df_person_tw: pd.DataFrame) -> float:
        """
        Compute the interaction ratio aggregated across TimeWindows.

        For each (date, tw_id) combination, compute the number of Person_TW
        nodes and the number of INTERACTS_WITH edges.  The total possible
        interacting pairs for a TimeWindow with n persons is n*(n-1)/2.  The
        aggregated ratio is the sum of actual interactions across all
        TimeWindows divided by the sum of possible pairs across those
        TimeWindows.  This measure better reflects the configured
        interact_ratio in the generator by removing the cross-TimeWindow
        combinations from the denominator.  The returned ratio is capped at
        1.0 to avoid values greater than 1 when more interactions than
        possible pairs are generated due to multiple overlaps.
        """
        # Count persons per (date, tw_id)
        # Only consider rows with non-null date and tw_id to avoid counting
        # missing values (e.g. for structural nodes).
        if df_person_tw.empty:
            return 0.0
        # Ensure the columns exist
        if "date" not in df_person_tw.columns or "tw_id" not in df_person_tw.columns:
            return 0.0
        # Compute per TW person counts
        person_counts = (
            df_person_tw
            .dropna(subset=["date", "tw_id"])
            .groupby(["date", "tw_id"])
            .size()
        )
        total_possible_pairs = 0.0
        for n in person_counts:
            total_possible_pairs += n * (n - 1) / 2.0
        # Count interactions per TW
        df_interacts = df_edges[df_edges["type"] == "INTERACTS_WITH"]
        if df_interacts.empty or total_possible_pairs == 0:
            return 0.0
        if "date" not in df_interacts.columns or "tw_id" not in df_interacts.columns:
            # If interactions lack date/tw_id, fall back to global ratio
            actual = len(df_interacts)
            return min(actual / total_possible_pairs, 1.0)
        interaction_counts = (
            df_interacts
            .dropna(subset=["date", "tw_id"])
            .groupby(["date", "tw_id"])
            .size()
        )
        total_interactions = interaction_counts.sum()
        # Compute ratio and cap at 1
        ratio = total_interactions / total_possible_pairs
        if ratio > 1.0:
            ratio = 1.0
        return ratio

    def check_movement(df_person: pd.DataFrame):
        """Return list of id_global that appear across multiple partition_ids."""
        moving = []
        for pid, group in df_person.groupby("id_global"):
            if group["partition_id"].nunique() > 1:
                moving.append(pid)
        return moving

    def check_interacts_same_camera_video(df_edges: pd.DataFrame, df_detected: pd.DataFrame) -> bool:
        """
        Ensure that every INTERACTS_WITH edge connects two Person_TW nodes
        that were detected in the same camera and same video segment.

        We build a mapping from Person_TW id to (camera_id, video_id) using
        DETECTED_IN edges and then verify that both ends of the
        INTERACTS_WITH edge share identical (camera_id, video_id).
        """
        # Build mapping: pid_tw -> (camera_id, video_id)
        mapping = {}
        # Only consider DETECTED_IN edges for persons
        # DETECTED_IN: source_id=pid_tw, dest_id=video_id
        for _, row in df_detected.iterrows():
            pid_tw = row['source_id']
            cam = row['camera_id']
            vid = row['destination_id']  # destination_id is the video_id
            # Keep only the first mapping per Person_TW
            if pid_tw not in mapping:
                mapping[pid_tw] = (cam, vid)
        # Check each INTERACTS_WITH edge
        for _, r in df_edges[df_edges['type'] == 'INTERACTS_WITH'].iterrows():
            pid1 = r['source_id']
            pid2 = r['destination_id']
            m1 = mapping.get(pid1)
            m2 = mapping.get(pid2)
            # If either participant lacks a DETECTED_IN mapping, fail
            if m1 is None or m2 is None:
                return False
            # Participants must share same camera and same video
            if m1 != m2:
                return False
        return True

    # Compute additional checks
    detected_df = df_rels[df_rels["type"] == "DETECTED_IN"]
    temporal_overlap_errors = check_temporal_overlap(df_rels, detected_df)
    temporal_overlap_status = "PASS" if not temporal_overlap_errors else "FAIL"

    missing_edge_types = check_edge_types(df_rels)
    row_counts_status = "PASS" if not missing_edge_types else "FAIL"

    # Verify partition integrity for core files
    partition_integrity_ok = check_partition_integrity_all(
        data_dir,
        [
            "nodes_person_TW.csv",
            "nodes_thing_TW.csv",
            "nodes_vehicle_TW.csv",
            "rels.csv",
        ],
    )
    partition_integrity_status = "PASS" if partition_integrity_ok else "FAIL"

    data_distribution_ok = check_data_distribution(df_person_tw, df_vehicle_tw, df_rels)
    data_distribution_status = "PASS" if data_distribution_ok else "FAIL"

    # Compute interaction ratio aggregated across TimeWindows.  This avoids
    # dividing by global person combinations, which would produce a
    # near-zero value for large datasets.  The ratio reflects the
    # proportion of interacting pairs within each TW across the entire
    # dataset.  If this aggregated denominator is zero, the ratio
    # defaults to 0.0.
    interaction_ratio_value = calculate_interaction_ratio_per_tw(df_rels, df_person_tw)
    moving_ids = check_movement(df_person_tw)
    # Ensure INTERACTS_WITH edges are within same camera and video
    same_camera_video_ok = check_interacts_same_camera_video(df_rels, detected_df)
    same_camera_video_status = "PASS" if same_camera_video_ok else "FAIL"

    # Determine final schema validity: includes structural/time checks and tw bucket checks
    schema_status = "PASS" if schema_ok else "FAIL"

    # Summarise results into a dictionary
    summary = {
        "temporal_overlap": temporal_overlap_status,
        "row_counts": "PASS" if row_counts_status == "PASS" else f"Missing edge types: {sorted(missing_edge_types)}",
        "partition_integrity": partition_integrity_status,
        "data_distribution": data_distribution_status,
        "interaction_ratio": round(interaction_ratio_value, 4),
        "movement_detected": moving_ids,
        "schema_valid": schema_status,
        "interact_same_camera_video": same_camera_video_status,
    }

    # Print summary as formatted JSON
    print("\nValidation summary:")
    print(json.dumps(summary, indent=2, ensure_ascii=False))


def main():
    ap = argparse.ArgumentParser()
    ap.add_argument("--data_dir", required=True, help="Path to directory containing generated CSVs")
    args = ap.parse_args()
    verify_dataset(args.data_dir)


if __name__ == "__main__":
    main()